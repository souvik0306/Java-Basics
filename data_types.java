public class data_types {  //ensure that the class name and file name are the same
    // everything in java is an object
    // class is a blueprint for an object
    // object can be defined as an instance of a class
    public static void main(String args[]) 
    // we need a main method to run a java program 
    // we write static so as to run the program without creating an object of the class
    // void is written to avoid any return 
    {
     double a = 50.2;
     System.out.println(a);

     long b = (long)a;
     System.out.println(b);

     double c = 50.343;
     System.out.println(c);

     float f = (float)c;
     System.out.println(f);
    }
    
}
